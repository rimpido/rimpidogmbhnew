                                                                      <p id="tools-modal-desc7">The bulk generation of WWI-reports is a challange, if you have big amounts of data. For one thing the WWI-server are competing with each other. Since at least, when a rawreport for a special SDB should be generatet fast, while a bulk generation ist processing, the standard is overstrained. For another the duration and the data volume of a bulk generation causes problems. So a offline data backup can abort a bulk generation. If this happens, normaly the only option is a restart. If the bulk generation of rawreports takes more than a week, this can be a big annoyance.<br><br>

DYN-WWI, that we can gladly implement specifically for you, based on an existing prototype, breaks down a bulk generation into small packages. These can be processed in minutes, allocated and monitored dynamicly to vacant WWI-servers. This allows to continue bulk generation automaticly and accurate after a offline data backup, overflowed document management systems etc. Thereby, DYN-WWI allows to perform bulk generation efficient and without attendance. In that way the EHS-department can dedicate their day-to-day business. The process of the bulk generation can be controlled via a on-screen dialog.</p>

